class Solution:
    def majorityElement(self, nums) -> int:
        new_dict = dict()
        for i in nums:
            new_dict[i] = new_dict.get(i, 0) + 1
        # key_name = max(new_dict, key=new_dict.get)

        key_value = 0
        key_name = nums[0]
        for i, j in new_dict.items():
            if key_value < j:
                key_name = i
                key_value = j
        return key_name


