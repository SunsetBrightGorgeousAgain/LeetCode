class Solution:
    def leastMinutes(self, n: int) -> int:
        new_list = list()
        new_list.append(0)
        new_list.append(1)
        for i in range(2, n+1):
            r = new_list[((i + 1) // 2)] + 1
            new_list.append(r)
        return new_list[n]

